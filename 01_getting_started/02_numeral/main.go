package main

import "fmt"

func main() {
	//var number int
	number := 10

	fmt.Printf("\n Integer is : %d ", number)
	fmt.Printf("\n Decimal is : %d ", number)
	fmt.Printf("\n Binary is : %b ", number)
	fmt.Printf("\n Octal is : %o ", number)
	fmt.Printf("\n Hexadecimal is : %x ", number)
	fmt.Printf("\n Float is : %3f ", 10.05)
	fmt.Printf("\n Char is : %c ", 'a')
	fmt.Printf("\n (Integer = 65) - %c ", 65)
	fmt.Printf("\n (Character = 'a')  %d ", 'a')
	fmt.Printf("\n")
}
